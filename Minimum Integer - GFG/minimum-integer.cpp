class Solution {
  public:
    int minimumInteger(int N, vector<int> &A) {
        // code here
        // 6 <= 3 * 3
        // 6 <= 3 * 2
        
        long long int sum = 0; int ans = INT_MAX; 
        
        for (auto it : A)
            sum += it;
            
        for(auto it : A) {
            if (sum <= (long long)it * N) {
                ans = min(ans, it); 
            }
        }
        return ans; 
    }
};


